#10) Crie uma classe que modele um número complexo para realizar aritmética com números complexos.

# Os números complexos têm a forma:
# parteReal + parteImaginaria * i, onde i é a raiz quadrada de -1.

# Utilize variáveis reais para representar as partes real e imaginária do número. A presença das partes real e imaginária é condição necessária para a criação do objeto. Um número complexo deve ser capaz de realizar:
# a) adição com outro número complexo: as partes reais são somadas de um lado e as partes imaginárias são somadas de outro;
#b) subtração com outro número complexo: a parte real do operando direito é subtraída da parte real do operando esquerdo e a parte imaginária do operando direito é subtraída da parte imaginária do operando esquerdo;
#c) multiplicação com outro número complexo;
#d) divisão com outro número complexo;
#e) fornecer uma representação em String do número na forma a + bi, onde a é a parte real e b é a parte imaginária.
# As fórmulas para a multiplicação e divisão de números complexos podem ser encontradas em http://en.wikipedia.org/wiki/Complex_number

